# sys 모듈
import sys

print("sys.argv: ", sys.argv) # Arguments Vector

# 일반적인 프로그래밍 언어
# 스칼라 : 하나의 값만을 가지는 것 - 문자열, 숫자, 불리언
# 벡터 : 여러 개의 값을 가질 수 있는 것 - 리스트, 딕셔너리

# 수학과 관련된 프로그래밍 언어 - 선형대수
# 스칼라 : 하나의 값만을 가지는 것
# 벡터 : 스칼라가 모인 것
# 텐서 : 많은 정의가 있지만, 일반적으로 벡터가 모인 것을 텐서라고 한다. 

