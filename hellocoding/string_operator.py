# 문자열 연결 연산자
# <문자열> + <문자열>
# NG : <숫자> + <문자열>
print("안녕" + "하세요")
# print("안녕" + 1)

# 문자열 반복 연산자 : *
# <숫자> * <문자열> or <문자열> * <숫자>
print("안녕" *3)

# 문자 선택 연산자 : []
# <문자열>[<숫자>]
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])
print("안녕하세요"[3])
print("안녕하세요"[4])

# - Zero Index : 파이썬
# - One Index

# 문자 범위 선택 연산자 : [:]
# <문자열>[<숫자>:<숫자>]
print("안녕하세요[1:4]:","안녕하세요"[1:4])
print("안녕하세요[1:]:","안녕하세요"[1:])
print("안녕하세요[:4]:","안녕하세요"[:4])

